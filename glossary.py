def lookup(term: str) -> str:
    """용어 설명 반환(없으면 빈 문자열)."""
    return GLOSSARY.get(term, "")


def footnotes(terms) -> str:
    """카드 하단에 붙일 각주 블록 생성. terms = 등장 순서 용어 리스트."""
    seen, lines = [], []
    for i, t in enumerate((x for x in terms if x in GLOSSARY), start=1):
        if t in seen:
            continue
        seen.append(t)
        lines.append(f"  {len(seen)}) {t}: {GLOSSARY[t]}")
    return "\n".join(lines)
